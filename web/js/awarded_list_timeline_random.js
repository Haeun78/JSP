const container = document.querySelector(".book-list");
prizeBooks.sort(() => Math.random() - 0.5).forEach(book => {
  const card = document.createElement("div");
  card.className = "book-card";
  card.innerHTML = `
    <div class="image-wrapper">
      <img src="${book.image}" alt="${book.title}">
    </div>
    <div class="info">
      <div class="title">${book.title}</div>
      <div class="desc">${book.author} · ${book.year} · ${book.prize}</div>
    </div>
  `;
  card.onclick = () => openModal(book);
  container.appendChild(card);
});

// 모달 작동
function openModal(book) {
  document.getElementById("modalTitle").textContent = book.title;
  document.getElementById("modalAuthor").textContent = "저자: " + book.author;
  document.getElementById("modalPrize").textContent = `${book.year} ${book.prize} 수상작`;
  document.getElementById("modalSummary").textContent = book.summary;
  document.getElementById("modalImage").src = book.image;
  document.getElementById("bookModal").style.display = "flex";
}

document.getElementById("modalClose").onclick = () => {
  document.getElementById("bookModal").style.display = "none";
};
